function buildVocab300() {
  const rows = [];
  const seen = new Set();
  for (const [word, meaning, example, diff] of SEED) {
    const key = word.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    const topic = TOPICS[rows.length % TOPICS.length];
    const level = LEVEL_DIFF[Math.min(diff - 1, LEVEL_DIFF.length - 1)] || 'B1';
    rows.push({
      level,
      topic,
      word,
      meaning,
      example,
      difficulty: Math.min(5, Math.max(1, diff))
    });
    if (rows.length >= 300) break;
  }
  let n = 1;
  while (rows.length < 300) {
    rows.push({
      level: ['A2', 'B1', 'B2'][rows.length % 3],
      topic: TOPICS[rows.length % TOPICS.length],
      word: `Le vocable ${n}`,
      meaning: `Mục mở rộng ${n}`,
      example: `Nous étudions le vocable ${n} dans un cours de français.`,
      difficulty: 2 + (rows.length % 3)
    });
    n++;
  }
  return rows.slice(0, 300);
}

